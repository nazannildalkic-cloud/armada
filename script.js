let calCurrent = new Date();
var calLocaleMap = { de: 'de-DE', tr: 'tr-TR', ar: 'ar', en: 'en-GB', fa: 'fa-IR' };

function renderCalendar() {
    const year = calCurrent.getFullYear();
    const month = calCurrent.getMonth();
    const title = document.getElementById('cal-month-year');
    var locale = calLocaleMap[window.currentLang] || 'de-DE';
    if (title) title.textContent = new Date(year, month, 1).toLocaleDateString(locale, { month: 'long', year: 'numeric' });

    const first = new Date(year, month, 1);
    let startDay = first.getDay() - 1;
    if (startDay < 0) startDay = 6;
    const daysInMonth = new Date(year, month + 1, 0).getDate();
    const prevMonthDays = new Date(year, month, 0).getDate();

    const grid = document.getElementById('cal-grid');
    if (!grid) return;
    grid.innerHTML = '';

    for (let i = 0; i < startDay; i++) {
        const d = document.createElement('div');
        d.className = 'cal-day other-month';
        d.textContent = prevMonthDays - startDay + 1 + i;
        grid.appendChild(d);
    }
    const today = new Date();
    for (let day = 1; day <= daysInMonth; day++) {
        const d = document.createElement('div');
        d.className = 'cal-day';
        d.textContent = day;
        const dateStr = `${year}-${String(month + 1).padStart(2, '0')}-${String(day).padStart(2, '0')}`;
        if (today.getFullYear() === year && today.getMonth() === month && today.getDate() === day) d.classList.add('today');
        d.addEventListener('click', function () {
            const formDate = document.getElementById('form-date');
            if (formDate) formDate.value = dateStr;
            document.getElementById('termin').scrollIntoView({ behavior: 'smooth' });
        });
        grid.appendChild(d);
    }
    const total = grid.querySelectorAll('.cal-day').length;
    const rest = total % 7 === 0 ? 0 : 7 - (total % 7);
    for (let i = 0; i < rest; i++) {
        const d = document.createElement('div');
        d.className = 'cal-day other-month empty';
        d.textContent = '';
        grid.appendChild(d);
    }
}

