// --- LIGHTBOX ---
let lightboxIndex = 0;
let galleryImages = [];

function openLightbox(index) {
    const items = document.querySelectorAll('#gallery-grid .gal-item img');
    if (!items.length) return;
    galleryImages = Array.from(items).map(img => img.src);
    lightboxIndex = typeof index === 'number' ? index : parseInt(index, 10) || 0;
    if (lightboxIndex < 0) lightboxIndex = galleryImages.length - 1;
    if (lightboxIndex >= galleryImages.length) lightboxIndex = 0;
    const lb = document.getElementById('lightbox');
    document.getElementById('lightbox-img').src = galleryImages[lightboxIndex];
    lb.setAttribute('aria-hidden', 'false');
    document.body.style.overflow = 'hidden';
    document.addEventListener('keydown', lightboxKeydown);
}
function closeLightbox() {
    document.getElementById('lightbox').setAttribute('aria-hidden', 'true');
    document.body.style.overflow = '';
    document.removeEventListener('keydown', lightboxKeydown);
}
function lightboxKeydown(e) {
    if (e.key === 'Escape') closeLightbox();
    if (e.key === 'ArrowLeft') lightboxPrev();
    if (e.key === 'ArrowRight') lightboxNext();
}
function lightboxPrev() {
    lightboxIndex = (lightboxIndex - 1 + galleryImages.length) % galleryImages.length;
    document.getElementById('lightbox-img').src = galleryImages[lightboxIndex];
}
function lightboxNext() {
    lightboxIndex = (lightboxIndex + 1) % galleryImages.length;
    document.getElementById('lightbox-img').src = galleryImages[lightboxIndex];
}
window.closeLightbox = closeLightbox;
window.lightboxPrev = lightboxPrev;
window.lightboxNext = lightboxNext;

